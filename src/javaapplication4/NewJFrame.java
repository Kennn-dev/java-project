    public void loadDataProductsByCategoryID(String id){
        String sql = "select * from products where products.cateID ="+id;
        Statement stmt;
        try {
            stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
           
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            while(rs.next()){
                int idProd = rs.getInt("productID");
                int price = rs.getInt("productPrice");
                String name = rs.getString("productName");
                int qty = rs.getInt("quantity");
                String decs =rs.getString("description");
                
                model.addRow(new Object[]{idProd, name,price,qty, decs});
            }
            jTable1.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
