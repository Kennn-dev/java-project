    public void deleteCategory(String id){
        try {
            String sql = "SELECT * from products where cateID ="+id;
            String sql1 = "DELETE FROM category WHERE cateID = "+id;
            String sql2 = "DELETE FROM products WHERE cateID = "+id;
           
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            int count = 0;
            while(rs.next()){
                count++;
            }
            if(count > 0){
                int reply = JOptionPane.showConfirmDialog(null, "Do you want delete all products", "Category still have Products !", JOptionPane.YES_NO_OPTION);
                if(reply == JOptionPane.YES_OPTION){
                    stmt.executeUpdate(sql2);
                    stmt.executeUpdate(sql1);
                    JOptionPane.showMessageDialog(this, 
                        "Delete Successfull ! Hit Reload button!");
                }else if(reply == JOptionPane.NO_OPTION){
                    return;
                }
            }else {
                stmt.executeUpdate(sql1);
                JOptionPane.showMessageDialog(this, 
                        "Delete Successfull ! Hit Reload button!"); 
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
